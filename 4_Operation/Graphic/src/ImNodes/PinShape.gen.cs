        Triangle = 2,
        /// <summary>
        /// The triangle filled pin shape
        /// </summary>
        TriangleFilled = 3,
        /// <summary>
        /// The quad pin shape
        /// </summary>
        Quad = 4,
        /// <summary>
        /// The quad filled pin shape
        /// </summary>
        QuadFilled = 5,
    }
}
