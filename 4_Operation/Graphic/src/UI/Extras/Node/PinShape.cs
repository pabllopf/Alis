namespace Alis.Core.Graphic.UI.Extras.Node
{
    /// <summary>
    ///     The pin shape enum
    /// </summary>
    public enum PinShape
    {
        /// <summary>
        ///     The circle pin shape
        /// </summary>
        Circle = 0,

        /// <summary>
        ///     The circle filled pin shape
        /// </summary>
        CircleFilled = 1,

        /// <summary>
        ///     The triangle pin shape
        /// </summary>
        Triangle = 2,

        /// <summary>
        ///     The triangle filled pin shape
        /// </summary>
        TriangleFilled = 3,

        /// <summary>
        ///     The quad pin shape
        /// </summary>
        Quad = 4,

        /// <summary>
        ///     The quad filled pin shape
        /// </summary>
        QuadFilled = 5
    }
}