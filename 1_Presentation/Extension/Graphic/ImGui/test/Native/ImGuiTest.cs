using System;
using Xunit;

namespace Alis.Extension.Graphic.ImGui.Test.Native
{
    public class ImGuiTest
    {
        [Fact]
        public void Test_ImGui_Version()
        {
            string version = ImGui.Native.ImGui.GetVersion();
            Assert.NotEqual("0", version);
        }
        
        [Fact]
        public void Test_ImGui_CreateContext()
        {
            IntPtr context = ImGui.Native.ImGui.CreateContext();
            Assert.NotEqual(IntPtr.Zero, context);
        }
    }
}